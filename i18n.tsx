type TranslationKey = keyof typeof translations.fi;

interface I18nContextValue {
  language: LanguageCode;
  setLanguage: (language: LanguageCode) => void;
  locale: string;
  speechLocale: string;
  t: (key: TranslationKey) => string;
  categoryName: (name: string) => string;
}

const I18nContext = createContext<I18nContextValue | null>(null);

const isLanguageCode = (value: string | null): value is LanguageCode => (
  Boolean(value && LANGUAGES.some((language) => language.code === value))
);

const detectBrowserLanguage = (): LanguageCode => {
  if (typeof navigator === 'undefined') return 'fi';

  const languageMap: Record<string, LanguageCode> = {
    fi: 'fi',
    sv: 'sv',
    en: 'en',
    uk: 'uk',
    et: 'et',
    ru: 'ru',
    se: 'se',
    sme: 'se',
    smn: 'se',
    sms: 'se',
  };

  for (const language of navigator.languages ?? [navigator.language]) {
    const normalized = language.toLocaleLowerCase().split('-')[0];
    const mapped = languageMap[normalized];
    if (mapped) return mapped;
  }

  return 'fi';
};

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [language, setLanguageState] = useState<LanguageCode>(() => {
    try {
      const saved = localStorage.getItem(LANGUAGE_STORAGE_KEY);
      return isLanguageCode(saved) ? saved : detectBrowserLanguage();
    } catch {
      return detectBrowserLanguage();
    }
  });

  useEffect(() => {
    try {
      localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    } catch {
      // Ignore storage errors.
    }
    document.documentElement.lang = language;
  }, [language]);

  const value = useMemo<I18nContextValue>(() => {
    const languageInfo = LANGUAGES.find((item) => item.code === language) ?? LANGUAGES[0];
    return {
      language,
      setLanguage: setLanguageState,
      locale: languageInfo.locale,
      speechLocale: languageInfo.speechLocale,
      t: (key) => translations[language][key] ?? translations.fi[key],
      categoryName: (name) => categoryTranslations[name]?.[language] ?? name,
    };
  }, [language]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error('useI18n must be used inside LanguageProvider');
  }
  return context;
};
